#include<stdio.h>
int main()
{
  int iNo1 = 10;
  float fValue = 3.14;
  double dData = 6.10;
  char cValue = 'M';

  printf("Interger Value is  %d\n",iNo1);
  printf("Float Value is  %f\n",fValue);
  printf("Double Value is  %lf\n",dData);
  printf("Character Value is  %c\n",cValue);

  return 0;
}

/*
    %d            Decimal                int 
    %f            Float                  float
    %lf           Long float             double
    %c            Chracter               char
    %s            String                 string
*/